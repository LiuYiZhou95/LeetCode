/*
 六大设计原则--单一职责
 什么是单一职责：如果一个类承担的职责越多，那么它被复用的可能性就越小，并且一个类承担的职责过多的话，这些职责就会耦合在一起，当其中一个职责发生改变时，就可能会影响到其他职责的运作，这样的话势必会影响到后期功能的优化或扩展。
 核心思想：分离职责，尽量小方法，每个方法只做一件事
 
 */
